function slugify(s) {
  // транслит для русских строк
  const map = {
    'а':'a','б':'b','в':'v','г':'g','д':'d','е':'e','ё':'e','ж':'zh','з':'z',
    'и':'i','й':'y','к':'k','л':'l','м':'m','н':'n','о':'o','п':'p','р':'r',
    'с':'s','т':'t','у':'u','ф':'f','х':'h','ц':'ts','ч':'ch','ш':'sh','щ':'sch',
    'ъ':'','ы':'y','ь':'','э':'e','ю':'yu','я':'ya',
  };
  return s.toLowerCase()
    .split('').map(c => map[c] !== undefined ? map[c] : c).join('')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 60);
}

function parseShopping(text) {
  const lines = text.split('\n');
  const lists = [];
  let currentList = null;
  let currentGroup = null;
  let idCounter = 0;

  function ensureDefaultGroup() {
    if (!currentList) return;
    if (currentGroup) return;
    currentGroup = { id: 'misc', emoji: '🛒', title: 'Дополнительно', items: [] };
    currentList.groups.push(currentGroup);
  }

  for (const line of lines) {
    // Заголовок списка: "# 🛒 Закупка 1 — главная (subtitle)"
    const listMatch = line.match(/^#\s+(?:🛒\s+)?(Закупка\s+\d+.*?)(?:\s*\((.+)\))?$/);
    if (listMatch) {
      currentList = {
        id: idCounter === 0 ? 'main' : 'mini',
        title: listMatch[1].trim(),
        subtitle: listMatch[2] || '',
        groups: [],
      };
      idCounter++;
      lists.push(currentList);
      currentGroup = null;
      continue;
    }

    if (!currentList) continue;

    // подзаголовок группы: "## 🥩 Мясо и рыба" — но не если без эмодзи (это могут быть заметки в конце)
    const groupMatch = line.match(/^##\s+([\u{1F300}-\u{1FAFF}\u{2600}-\u{27BF}]+)\s+(.+)$/u);
    if (groupMatch) {
      currentGroup = {
        id: slugify(groupMatch[2]),
        emoji: groupMatch[1],
        title: groupMatch[2].trim(),
        items: [],
      };
      currentList.groups.push(currentGroup);
      continue;
    }

    // если встретился любой другой ## (заметки типа "Ориентир по бюджету") — закрываем текущий список,
    // чтобы дальнейшие пункты не попали туда
    if (/^##\s+/.test(line)) {
      currentList = null;
      currentGroup = null;
      continue;
    }

    // элемент: "- [ ] что угодно"
    const itemMatch = line.match(/^-\s+\[\s\]\s+(.+)$/);
    if (itemMatch) {
      ensureDefaultGroup();
      let raw = itemMatch[1].trim();
      const starred = /⭐/.test(raw);
      raw = raw.replace(/⭐/g, '').trim();
      const dashIdx = raw.indexOf(' — ');
      const name = (dashIdx > 0 ? raw.slice(0, dashIdx) : raw)
        .replace(/\*\*/g, '')
        .trim();
      currentGroup.items.push({
        id: slugify(name) + '-' + currentList.id + '-' + currentGroup.items.length,
        text: raw.replace(/\*\*/g, '').replace(/\*([^*]+)\*/g, '$1'),
        starred,
      });
    }
  }

  // отбрасываем пустые группы
  for (const list of lists) {
    list.groups = list.groups.filter(g => g.items.length > 0);
  }

  return lists;
}

